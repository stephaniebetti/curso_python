'''Tipo de dado -> bool (boolean)'''
# Ao questionar algo em um programa,
# so existem duas respostas possiveis:
# sim (True) ou nao (False).

'''existem operadores p/ questionar'''
# dentre eles o ==, que eh um operador logico
# que questiona se o valor eh igual a outro.

print(10==10) # Sim -> True (T maiusculo)
print(10==11) # Nao -> False (F maiusculo)
print(type(10==11)) # terminal: <class 'bool'>
print(type(False)) # terminal: <class 'bool'>
print(type(True)) # terminal: <class 'bool'>